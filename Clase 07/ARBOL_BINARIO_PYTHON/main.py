from arbol import Arbol

####################################################################################################
####################################################################################################
# Creo la raiz
####################################################################################################
####################################################################################################

arbol_numeros = Arbol(5)

####################################################################################################
####################################################################################################
# Agrego lista
####################################################################################################
####################################################################################################

lista_agregar = [1500,3,9,25,85,166,21,77,0,45,20]

for i in lista_agregar:
    arbol_numeros.agregar(i)

####################################################################################################
####################################################################################################
# Imprimo arbol en los distintos ordenes
####################################################################################################
####################################################################################################

arbol_numeros.preorden()
arbol_numeros.inorden()
arbol_numeros.postorden()


####################################################################################################
####################################################################################################
# Aplico el metodo buscar con un input para ingresar en terminal el numero y un while para que no corte
####################################################################################################
####################################################################################################

while True:
    busqueda = int(input("Ingresa un número para buscar en el árbol: "))
    n = arbol_numeros.buscar(busqueda)
    if n is None:
        print(f"{busqueda} no existe")
    else:
        print(f"{busqueda} sí existe")